package model;

public class Employee {
	private String emp_id;
	private String emp_name;
	private String emp_start_date;
	private String emp_address;
	private String emp_manager_id;
	private String emp_branch;

	public String getEmp_id() {
		return emp_id;
	}

	public void setEmp_id(String emp_id) {
		this.emp_id = emp_id;
	}

	public String getEmp_name() {
		return emp_name;
	}

	public void setEmp_name(String emp_name) {
		this.emp_name = emp_name;
	}

	public String getEmp_start_date() {
		return emp_start_date;
	}

	public void setEmp_start_date(String emp_start_date) {
		this.emp_start_date = emp_start_date;
	}

	public String getEmp_address() {
		return emp_address;
	}

	public void setEmp_address(String emp_address) {
		this.emp_address = emp_address;
	}

	public String getEmp_manager_id() {
		return emp_manager_id;
	}

	public void setEmp_manager_id(String emp_manager_id) {
		this.emp_manager_id = emp_manager_id;
	}

	public String getEmp_branch() {
		return emp_branch;
	}

	public void setEmp_branch(String emp_branch) {
		this.emp_branch = emp_branch;
	}

}
